/** 依任務代碼取出轉換原則；找不到回傳空字串 */
export function transformRule(taskCode) {
  if (!taskCode) return '';
  // M3-08 涵蓋 2.3 / 3.3 / 4.3 三個等級任務，容忍變體代碼
  const key = RULES[taskCode] ? taskCode : (/^M3-08/.test(taskCode) ? 'M3-08' : null);
  if (!key) return '';
  return RULES[key] + (LOOSE_TASKS.test(key) ? LOOSE_NOTE : '');
}

